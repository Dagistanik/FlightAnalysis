package org.example;

public class Ticket {
    private String carrier;
    private String departure;
    private String arrival;
    private int duration;
    private int price;

    public String getCarrier() {
        return carrier;
    }

    public String getDeparture() {
        return departure;
    }

    public String getArrival() {
        return arrival;
    }

    public int getDuration() {
        return duration;
    }

    public int getPrice() {
        return price;
    }

}
